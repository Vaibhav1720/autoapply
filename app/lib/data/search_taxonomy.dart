/// Curated dropdown / autocomplete data for the search hero.
///
/// Keeping these client-side (a) avoids a network roundtrip for the
/// suggestions, (b) lets the user search even when offline, (c) keeps
/// the lists small enough to ship in the bundle (~20KB total).
///
/// All values are stored as plain strings (the same shape the backend
/// already accepts). For locations we deliberately use the canonical
/// "City, Region, Country" form because that matches what LinkedIn,
/// Lever, Greenhouse, and the company scrapers all expect.
library;

class IndustryOption {
  final String id; // stable, lowercase, sent to backend
  final String label; // display label
  final String emoji;

  const IndustryOption(this.id, this.label, this.emoji);
}

